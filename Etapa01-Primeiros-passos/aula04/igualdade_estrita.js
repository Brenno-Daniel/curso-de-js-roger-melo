const ageB = 22

// 'igual a' e 'diferente de'
console.log(ageB == 22)
console.log(ageB == '22')
console.log(ageB != 22)
console.log(ageB != '22')

// 'igual a, e do mesmo tipo' e 'diferente de, e do mesmo tipo'
console.log(ageB === 22)
console.log(ageB === '22')
console.log(ageB !== 22)
console.log(ageB !== '22')
